/**
 * Contains all the labels for the popover
 *
 * articles: the label above the article list
 * links: the label above the link list
 * iconAlt: the alt property for the icon element (and tooltip)
 * comingSoon: the message to be displayed if no content was found
 */
export class PopoverLabels {
    articles?: string | null = '';
    links?: string | null = '';
    iconAlt?: string | null = '';
    comingSoon?: string | null = '';
}
